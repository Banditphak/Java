package day4;

public class Main {
    public static void main(String[] args) {
    			// this is java comments
    			System.out.println("Apple");    // Apple
    			System.out.println("--------"); // --------
    			
    			System.out.print("Banana"); 
    			System.out.print("Kiwi");   //BananaKiwi------
    			System.out.println("------");
    			
    			// System.out.println() will add new line after the print
    			// System.out.print() will not add new line(so everything will in the same line) 
    			
    			System.out.println(+(187%100)+" cents in coins:\n3 quarters\n1 dimes\n0 nickels\n2 pennies");
    			
    		    //87 cents in coins:
    			//3 quarters
    			//1 dimes
    			//0 nickels
    			//2 pennies
    			
     }
}
